#include "solong.h"

void	get_coins(t_game *g, t_map *map)
{
	int	r;
	int	c;

	r = 0;
	while (r < map->r)
	{
		c = 0;
		while (c < map->c)
		{
			if (map->map[r][c] == 'C')
				g->pg.coins++;
			c++;
		}
		r++;
	}
}
